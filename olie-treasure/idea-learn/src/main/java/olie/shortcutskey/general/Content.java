    /**
     * Open corresponding tool window : 工具窗口栏快捷键
     * Alt + [0-9]
     * Alt + 0 : 消息窗口（Message Tool）
     * Alt + 1 : 项目窗口（Project Tool）
     * Alt + 2 : 收藏窗口（Favorites Tool）
     * Alt + 3 : 查找窗口（Find Tool）
     * Alt + 4 : 运行窗口（Run Tool）
     * Alt + 5 : 调试窗口（Debug Tool）
     * Alt + 6 : 备忘录窗口 （Todo Tool）
     * Alt + 7 : 对象结构窗口（Structure Tool）
     * Alt + 9 : 版本控制窗口（Version Tool）
     */


    /**
     * Save All : 保存
     * Ctrl + S
     */


    /**
     * Synchronize  : 检测所有外部更改的文件，并从磁盘重新加载
     * Ctrl + Alt + Y
     */


    /**
     * Toggle maximizing editor : 最大化编辑器窗口
     * Ctrl + Shift + F12
     */

    /**
     * Inspect current file with current profile : 检查当前文件
     * Alt + Shift + I
     */

    /**
     * Quick switch current scheme  : 快速切换模式
     * Ctrl + BackQuote (`)
     */

    /**
     * Open Settings dialog   : 打开设置窗口
     * Ctrl + Alt + S
     */

    /**
     * Open Project Structure dialog    : 打开项目结构窗口
     * Ctrl + Alt + Shift + S
     */

    /**
     *  Find Action   : 查找命令
     *  Ctrl + Shift + A
     */



